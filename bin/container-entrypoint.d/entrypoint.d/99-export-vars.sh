#!/usr/bin/env bash

for DEFAULT_VAR_NAME in $(compgen -v | grep '_WCMTECH_DEFAULT$'); do

	BASE_VAR_NAME="${DEFAULT_VAR_NAME%_WCMTECH_DEFAULT}"
	DEFAULT_VAR_VALUE="${!DEFAULT_VAR_NAME}"
	CURRENT_VAR_VALUE="${!BASE_VAR_NAME}"

	if [ -z "$CURRENT_VAR_VALUE" ]; then
		export "$BASE_VAR_NAME"="$DEFAULT_VAR_VALUE"
	else
		export "$BASE_VAR_NAME"="$CURRENT_VAR_VALUE"
	fi

	CLEANUP_VAR_LIST="${CLEANUP_VAR_LIST:+$CLEANUP_VAR_LIST:}$BASE_VAR_NAME"

done

true
